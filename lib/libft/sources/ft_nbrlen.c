#include "libft.h"

size_t	ft_nbrlen(int nbr)
{
	size_t	len;

	len = 0;
	if (nbr < 0)
		len = 1;
	while (nbr)
	{
		++len;
		nbr /= 10;
	}
	return (len);
}
